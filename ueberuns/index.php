<div class="page-title">Wir über uns</div>
<div class="page-section">Nach dem Blick auf den Pluto gibt's zur Feier schon mal Pizza...</div>
<div class="page-text">...so lautete ein Artikel etwas salopp in der Frankfurter Neuen Presse vom September 1992 über unsere Sternwarte. Nicht etwa, dass wir uns nicht gefreut hätten über die Publizität unserer astronomischen Aktivitäten - aber der Blick auf den Pluto ist uns von der Kuppel des Schülerhauses leider nicht möglich. Dazu sind die Beobachtungsbedingungen in Frankfurt nicht gut genug und unsere Instrumente reichen für ein solch lichtschwaches Objekt nicht aus.
<br/><br/>
Wenn wir auch nicht Pluto beobachten können, so haben doch schon hunderte von Schülern und Schülerinnen von hier zum ersten Male in ihrem Leben Krater und Gebirge auf dem Mond, Reste einer Sternexplosion oder die Galaxie in 2 Millionen Lichtjahren Entfernung gesehen.
<br/><br/>
Die Astronomie-AG der Liebigschule besteht nun schon seit über 30 Jahren mit regelmässigen Kursen und Beobachtungen am Montagabend für Schülergruppen verschiedenen Alters.<br/><br/><br/></div>

