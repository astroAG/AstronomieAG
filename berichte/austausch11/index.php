<div class="page-text">Auf Anfrage von Frau Terhorst, Mathematik- und Franz&ouml;sischlehrerin der Liebigschule, hie&szlig;en wir am 9. Mai 2011 die Teilnehmer des deutsch-franz&ouml;sischen Austauschs zwischen Mulhouse und Frankfurt bei uns herzlich willkommen.
<br/><br/>
Mit dabei waren zw&ouml;lf franz&ouml;sische und acht deutsche Sch&uuml;ler und Sch&uuml;lerinnen. Von unserer Seite war nat&uuml;rlich Werner Ziegs, der Leiter der AG, anwesend. Er bekam tatkr&auml;ftige Unterst&uuml;tzung durch zwei Mitglieder der III. Gruppe, Nils Paffen und Patrick Hemberger.
