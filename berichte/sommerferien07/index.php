<div class="page-title">Vorträge vor den Sommerferien 2007</div>

<div class="page-text">Es war schon eine sehr nervenaufreibende Angelegenheit: Werden die Wolken in letzter Sekunde vielleicht doch noch aufreißen? Werden wir heute Abend noch beobachten können?
Diesen Abend bekamen wir nicht nur Besuch von Liebigschülern und deren Eltern, sondern auch von einigen Lehrern und unserem Schulleiter. Insgesamt hatten wir so knapp 20 Besucher.
<br/><br/>
Unsere geplante Beobachtung fiel leider ins Wasser. Da der Himmel um 19.00 Uhr im Sommer noch nicht dunkel genug ist, um Planeten und Co zu sehen, wollten wir direkt zu Anfang mit einem speziellen Teleskop unsere Sonne beobachten. Leider machte uns das Wetter aber auch hier einen Strich durch die Rechnung.
<br/><br/>
Trotzdem war der Abend ein voller Erfolg. Neben den drei Vorträgen (die alle von AG-Mitgliedern vorbereitet, erstellt und gehalten wurden) haben wir außerdem einige unserer besten Astrofotos ausgedruckt, gerahmt und ausgestellt. Hinzu kam die Live-Demonstration einer Aufnahme am Teleskop. Dafür hat Moritz sein 5-Zöller und seine modifizierte Kamera mitgebracht und in der Bibliothek aufgebaut.
<br/><br/>
Der letzte Punkt des "Events" bildete -neben der offenen Fragerunde- das Quiz. In einem Multiple-Choice-Test wurden einige Punkte der drei Vorträge "abgefragt". Natürlich musste beziehungsweise durfte jeder Anwesende mitmachen. Auch die Lehrer. Unter den Besten wurden dann insgesamt neun Preise verlost. Darunter eigene Aufnahmen als Poster, Bausätze für ein Sonnenteleskop und Bücher.
<br/><br/>
</div>

