<div class="page-title">Vorträge & Beobachtung anlässlich der Saturnopposition 2007</div>

<div class="page-text">Es war schon eine sehr nervenaufreibende Angelegenheit: Werden die Wolken in letzter Sekunde vielleicht doch noch aufreißen? Werden wir heute Abend noch beobachten können?
<br/><br/>
Doch fangen wir von vorne an: Am Dienstag, den 13. Februar 2007, hielt die Astro-AG abends um 20.00 Uhr drei Vorträge zum Thema Saturn. Diese wurden von einigen Teilnehmern der AG (III. Gruppe) vorbereitet und schließlich auch selbst gehalten. Anschließend sollte eine Beobachtung mit unserem 10-Zöller Meade folgen. Doch das schien am Anfang alles andere als gut zu gehen.
<br/><br/>
Eingeladen waren alle Liebigschüler, deren Freunde und Verwandte und natürlich auch alle Nicht-Liebigschüler. (Berichtet wurde davon am Tag der offenen Tür, in den Liebig e-news, auf Plakaten in der Schule und natürlich auf unserer Homepage)
<br/><br/>
Und die Arbeit hatte sich gelohnt: Wir hatten schon lange nicht mehr so viele Besucher bei einer unserer Veranstaltungen.
<br/><br/>
Nach den Vorträgen kam die alles entscheidende Frage: Wie sieht es am Himmel aus? Und anscheinend hatte man unsere Wünsche erhört. Die Wolken sind genau an der Stelle aufgerissen, an der Saturn am Nachthimmel stand. Unser großes Meade konnten wir dann zwar nicht mehr aufbauen (zu gefählrich wegen Regengefahr, außerdem lange Aufbau-Zeit), aber auch die Beobachtung durch das Teleskop in der Kuppel hat Jung und (fast ;-) Alt Spaß gemacht...
<br/><br/>
Abschließend finden Sie noch ein paar Bilder vom gestrigen Abend, sowie das Material zum Nochmal-Ansehen und ein paar weiterführende Infos (für die besonders Interessierten...)
<br/><br/>
<i>Wie würden uns freuen wenn wir auch SIE bei einem unserer nächsten Themen-Abende in der Liebigschule Wilkkommen heißen dürfen!</i><br/><br/>
</div>

