<div class="page-title">Beobachtung an der Erdfunkstelle Usingen am 15. Oktober 2011</div>

<div class="page-text">Am 15. Oktober 2011 trafen sich die Astro- AG und WU Gruppen der Liebigschule bei der Erdfunkstelle in Usingen, um den Nachthimmel zu beobachten. Um 16.30 wurden wir vom Leiter der Erdfunkstelle empfangen. Dieser zeigte uns die Parkpl&auml;tze und &ouml;ffnete f&uuml;r uns das Besucherhaus. Auf dem etwa 140 ha gro&szlig;en Gel&auml;nde sind 90 Antennen verteilt.
<br/><br/>
Auf einer Wiese direkt neben einer der Antennen begannen wir mit dem Aufbau der Ausr&uuml;stung. Neben Feldstechern von AG- und WU-Gruppenmitgliedern,  war auch das Dobson Lightbridge Teleskop schnell aufgebaut. Zwei weitere Teleskope von Patrick Hemberger, einem AG-Mitglied, und Ludwig Kupec, einem Kollegen unseres AG-Leiters, waren nach der Platzierung auf einem Stativ einsatzbereit. Herr Ziegs und Herr Kupec installierten zum Schluss noch jeweils eine Kamera, um den Nachthimmel in einer Zeitrafferaufnahme festzuhalten festzuhalten.
<br/><br/>
Nach dem Aufbau der Technik bekamen wir vom Leiter der Erdfunkstelle das Geb&auml;ude der Firma Media Broadcast gezeigt. Das Unternehmen sendet mit Hilfe der Antennen der Erdfunkstelle internationale Fernsehprogramme in die ganze Welt. Er erkl&auml;rte uns, dass das Signal der TV-Sender mithilfe der Antenne zu einem Sateliten transportiert wird, welcher es dann weltweit versendet.
<br/><br/>
Zur&uuml;ck im Besucherzentrum empfing uns die Mutter von Martin K&ouml;llen, einem sehr interessierten WU-Mitglied, mit einer warmen Mahlzeit. Gest&auml;rkt ging es anschlie&szlig;end zur&uuml;ck zum Beobachtungsplatz. Inzwischen war es dunkel, langsam kam Frost auf. Die Beobachtung des Sternenhimmels konnte losgehen. Der Mond war das erste Objekt was wir an diesem Abend durch die Teleskope betrachteten. Die Vergr&ouml;&szlig;erung sorgte f&uuml;r eindrucksvolle Bilder der Mondkrater und der Oberfl&auml;che. Mithilfe von Nachf&uuml;hrung und Skyscout waren schnell weitere Objekte gefunden. Jupiter und seine Monde waren wunderbar zu erkennen. Wer gerade keinen Platz an einem Teleskop ergattern konnte, machte es sich in einem der Campingst&uuml;hle bequem um den Sternenhimmel und vereinzelt sogar eine Sternschnuppe zu entdecken. Die Teleskope und die Kameras mussten vor kondensierter Feuchtigkeit und sp&auml;ter vor Vereisung gesch&uuml;tzt werden. Das gelang mit einer kleinen Heizmanschette, die wir mit einer Autobatterie betrieben.
<br/><br/>
Die Beobachtung wurde von einem Filmteam des Hessischen Rundfunks begleitet, welches Aufnahmen f&uuml;r die  Serie &quot;Bilderbogen&quot; drehte. Die Zeitrafferaufnahme ist unter <a href="http://www.youtube.com/watch?v=v9blPHbfSHI">Youtube</a> anschaubar.
