<div class="page-title">Unsere Neuen: Coronado und 12" Lightbridge</div>

<div class="page-text">Seit Sommer 2007 haben wir in der Astro AG der Liebigschule 2 neue Instrumente, die wir durch eine großzügige Geldspende der Deutschen Bank anschaffen konnten.
<br/><br/>
Die Deutsche Bank förderte unser Schulprojekt, welches Schüler im Bereich der Naturwissenschaften weiterbildet. Dabei ist das Ziel, Naturwissenschaften ansprechend und verständlich zu vermitteln.
<br/><br/>
Die Wahl fiel auf ein Newton-Teleskop in Dobson-Bauweise. Bei diesem Gerät muss man das Himmelsobjekt zwar selbst aufsuchen, dafür ist das Gerät aber sehr transportabel und damit schnell überall aufzustellen. Schüler können es selbst bedienen. Es ist ein MEADE 12" Lightbridge und durch die große Öffnung hervorragend Lichtstark.
<br/>
Die zweite Anschaffung ist ein spezielles Sonnenteleskop. Damit kann man am Tage die Oberflächenerscheinungen der Sonne beobachten. Diese Teleskopart gibt es für Amateure erst seit wenigen Jahren. Man kann durch einen speziellen Filter die h-alpha Strahlung des Wasserstoffs sichtbar machen. Beobachtbar sind die Eruptionen (Ausbrüche) am Sonnenrand. Dadurch besteht die Möglichkeit der experimentellen Beobachtung im astronomischen Tagesunterricht.<br/><br/>
</div>

