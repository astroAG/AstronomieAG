<div class="page-section">Datei Upload/Downlaod</div>
<div class="page-text">Der Dateiupload ist dafür gedacht, allen Mitgliedern der AG Dateien zur Verfügung zu stellen. Beachtet aber bitte, dass der Speicherplatz nicht unbegrenzt ist.<br/>
<ul>
    <li>Um Dateien <u>hochzuladen</u>, mit dem Button "Durchsuchen" die Datei auswählen und anschließend auf "Datei hochladen!" klicken. Folgende Dateitypen sind erlaubt: zip, jpg, png, gif, doc, xls, ppt, odt, ods, odp, txt und pdf. Eine Datei darf nicht größer sein, als 4MB. Wurde die Datei erfolgreich hochgeladen, wird eine Meldung ausgegeben. Die Datei steht nun in der Liste zu Verfügung.</li>
    <li>Wenn man auf den Dateinamen klickt kann man sich (auch abhängig von verwendetem Browser) die Datei <u>ansehen oder herunterladen</u>.</li>
    <li>
Um eine hochgeladene Datei vom Server (und aus der Liste) zu <u>löschen</u> einfach auf das 'x' hinter einem Eintrag klicken (Vorsicht: die Datei wird ohne Nachfragen sofort und unwiderruflich gelöscht!).</li></ul></div>

